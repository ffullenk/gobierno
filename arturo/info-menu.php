		    <h2>introducci&Oacute;N </h2>
		    <p>El presente trabajo corresponde a una alianza estrat&eacute;gica de las instituciones SERPLAC Y GOBIERNO REGIONAL DE LA REGION DE COQUIMBO, para difundir en un solo medio el trabajo recopilado durante m&aacute;s de un a&ntilde;o y medio de diversas fuentes bibliogr&aacute;ficas, con el objetivo de facilitar la comprensi&oacute;n y la labor de profesionales, t&eacute;cnicos y estudiantes de la Regi&oacute;n en sus respectivas labores en la tem&aacute;tica de g&eacute;nero.</p>
		    <p>El &eacute;nfasis est&aacute; dado desde la perspectiva de la planificaci&oacute;n en diversos &aacute;mbitos del desarrollo del pa&iacute;s y presenta una l&oacute;gica que permita facilitar el trabajo y contribuir a una efectiva incorporaci&oacute;n del Enfoque de G&eacute;nero en la labor diaria.</p>
		    <p>Este trabajo est&aacute; concebido como un proceso de mejora continua y de incorporaci&oacute;n y renovaci&oacute;n de la informaci&oacute;n y documentaci&oacute;n incluida.</p>
