          <h1> RESPONSABILIDAD PENAL DE LAS PERSONAS JUR&Iacute;DICAS POR EL DELITO DE COHECHO NACIONAL O INTERNACIONAL</h1>
          <hr />

<br />
<h2 >Mesa Regional de Coordinaci&oacute;n del SNIT</h2>                            
<p>
Desde Diciembre de 2009 se encuentra vigente en Chile la Ley 20.393, que establece un concepto absolutamente nuevo en nuestro ordenamiento jur&iacute;dico: la responsabilidad penal en que pueden incurrir las personas jur&iacute;dicas por la comisi&oacute;n de alguno de estos delitos:
</p>

<p>	1. Cohecho a funcionarios p&uacute;blicos, nacionales o extranjeros;</p>
<p>	2. Lavado de activos, y</p>
<p>	3. Financiamiento del terrorismo.</p>
<p>Las penas que por la comisi&oacute;n de dichos delitos pueden aplicarse a una empresa abarcan una amplia gama, que entre otras sanciones incluye multas, prohibiciones temporales o permanentes de celebrar contratos con el Estado, inclusi&oacute;n en una lista de entidades sancionadas, y hasta la disolucii&oacute;n de la persona jujur&iacute;dica (esto &uacute;ltimo en determinados casos respecto del delito de lavado de activos).</p>
<p>Espec&iacute;ficamente en lo concerniente al delito de cohecho de funcionarios p&uacute;blicos, cabe se&ntilde;alar, desde otro punto de vista, que los est&aacute;ndares actuales del comercio, tanto nacional como internacional, hacen altamente conveniente y beneficioso para una empresa seria el contar con mecanismos preventivos respecto de este delito, a lo cual tambi&eacute;n se refiere la Ley 20.393, en su art&iacute;culo 4.</p>
<p>La prevenci&oacute;n sanci&oacute;n del delito de cohecho a funcionarios p&uacute;blicos en el &aacute;mbito internacional, adem&aacute;s forma parte de las obligaciones asumidas por nuestro pa&iacute;s en el marco de la Convenci&oacute;n para Combatir el Cohecho a Funcionarios P&uacute;blicos Extranjeros en las Transacciones Comerciales Internacionales, de la OCDE.</p>
<p>Divulgar estos temas en el sector privado, incluyendo los concernientes a los mecanismos de prevenci&oacute;n contenidos en la Ley 20.393, resulta imprescindible y urgente, tanto para reforzar el &aacute;mbito preventivo, como para que el empresariado est&eacute; debidamente informado acerca del alcance de la ya mencionada ley, que, como se dijo al inicio, est&aacute; plenamente vigente desde el 2 de Diciembre de 2009. Para ello es que el Ministerio de Relaciones Exteriores tiene la intenci&oacute;n de efectuar, en regiones y con el apoyo de autoridades locales, reuniones con representantes del sector privado local.</p>


