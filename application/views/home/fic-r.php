<h3>2. Énfasis del Concurso</h3>
<p>El Fondo de Innovación para la Competitividad de asignación regional (FIC-R) es un programa incluido en la Ley de Presupuestos del Sector Público de cada año, en donde el componente regional es administrado por la Subsecretaría de Desarrollo Regional y posteriormente por cada Gobierno Regional, cuyo propósito es financiar iniciativas de innovación destinadas a incrementar la competitividad del país y sus regiones, en coherencia con la Estrategia Nacional de Innovación.</p>

<p>
Desde 2008, un monto equivalente al 25% de los recursos FIC nacional se ha destinado a la provisión FNDR como Fondo de Innovación para la Competitividad (en adelante FIC-R), el cual que es parte del Programa de Inversión Pública de los Gobierno Regionales.</p>

<p>
La Provisión FIC-R constituye un marco de recursos públicos regionales destinados a promover la competitividad de los diferentes sectores productivos regionales, el desarrollo económico regional y como resultado de esto, el mejoramiento en la calidad de vida de las personas y el incremento de oportunidades para su desarrollo. Para el logro de este objetivo, las Universidades y Centros de Investigación juegan un rol clave, siendo los generadores de conocimientos y tecnologías que deben ser puestas en valor para la sociedad.</p>

<p>
La cartera FIC-R 2012 está orientada y es coherente con el desarrollo de la Estrategia Regional de Innovación, que se elabora en el marco del Proyecto Red y que busca fundamentalmente disminuir las brechas detectadas en el Sistema Regional de Innovación y que han sido identificadas en la fase de Diagnóstico, en virtud de lo anterior, las propuestas que postulen al FIC-R deben tener una clara orientación y evidente impacto regional en los principales sectores productivos y estratégicos, buscando soluciones “innovadoras” a problemas u oportunidades presentes o futuras, que nos permitan avanzar hacia el desarrollo.</p>

<p>
Los lineamientos estratégicos transversales a nivel regional que se deben tener en cuenta a la hora de formular un proyecto para la postulación al FIC-R 2012, son los definidos en los siguientes instrumentos de planificación regional:</p>

<p>
La Estrategia Regional de Desarrollo – Región de Coquimbo al 2020, el Plan Regional de Gobierno 2010-2014, la Política Regional en Investigación Científica, Desarrollo e Innovación Tecnológica, la Política de Desarrollo Rural Campesina de la Región de Coquimbo y lo dispuesto en las presentes bases que dicen relación con las brechas detectadas en la fase de diagnóstico del Proyecto RED para la elaboración de la Estrategia Regional de Innovación. (Documento disponible en www.gorecoquimbo.gob.cl - link Proyecto RED).</p>

<p>3. ¿Qué es el FIC?</p>

<p>
El Fondo de innovación para la competitividad (FIC) se crea el año 2006 y es a partir del año 2008 que parte el FIC-R que es el Fondo de Innovación de la Competitividad de Asignación Regional, el FIC es un Instrumento para la aplicación de políticas nacionales y regionales de innovación que busca incrementar la competitividad del país sus regiones orientado a fortalecer el sistema nacional de innovación en regiones, con un sentido estratégico a la acción pública del Estado.</p>

<p>
Las regiones y particularmente la región de Coquimbo deben fortalecer su sistema regional de innovación, tenemos grandes desafíos que afrontar, solo como elemento de contexto, en el último Índice de Competitividad Regional (ICR 2008) la región se encuentra en décimo segundo lugar del ranking, bajando del sexto lugar que ocupaba en la medición del año 2003. Particularmente en el factor innovación ciencia y tecnología del ICR, la región se ubica en el penúltimo lugar.</p>

<p>
Esperamos que el FIC, se transforme en un instrumento detonador de cambios y transformaciones, actuando como acelerador de procesos, dando respaldo, soporte y financiamiento constante al sistema regional de innovación.</p>

<h3>4. ¿Qué financia el FIC?</h3>

<p>El FIC tiene una fuerte orientación a mejorar la competitividad regional a través de los mejores resultados económicos regionales y un fortalecimiento en la base empresarial a través de mejoras en la productividad.</p>

<h3>- El FIC financia particularmente la:</h3>

<ul>

<li>Investigación aplicada.</li>
<li>Innovación en las empresas.</li>
<li>Difusión y transferencia tecnológica.</li>
<li>Aceleración del emprendimiento innovador.</li>
<li>Formación y atracción de recursos humanos especializados.</li>
<li>Fortalecimiento de redes para la innovación y equipamiento de apoyo a la competitividad.</li>
<li>Fomento de la cultura de innovación el emprendimiento innovador.</li>

</ul>


<h3>5. ¿Quiénes pueden postular a los recursos FIC - Concurso Universidad y Centros 2012?</h3>

<p>
Universidades estatales o reconocidas por el Estado o, aquellas instituciones incorporadas en el registro de centros, para la realización de actividades de I+D establecidas en la Ley Nro. 20.241, y las instituciones que cumplan con los requisitos exigidos en el Decreto Nro. 68, de 23 de febrero de 2009, del Ministerio de Economía, Fomento y Reconstrucción, y sus modificaciones.
6. Coherencia con el Proyecto Red y la Estrategía Regional de Innovación
¿Qué es el proyecto RED Región de Coquimbo?</p>

<p>
Proyecto RED es parte del Programa de Innovación y Competitividad Unión Europea-Chile, que es coordinado por Agencia de Cooperación Internacional de Chile (AGCI) y ejecutado por la Subsecretaría de Desarrollo Regional (SUBDERE), con el apoyo de la Subsecretaría de Economía, InnovaChile de CORFO, CONICYT y los Gobiernos Regionales de siete regionales del país, con el objetivo de promover la innovación y el emprendimiento en beneficio del desarrollo regional del país.</p>

<p>
Proyecto RED, es una metodología y red de apoyo, basado en el know how europeo, para crear las condiciones que necesitan las regiones y sus empresas para innovar y ser competitivas.</p>

<p>
El objetivo central de este proyecto es que se llegue a profundizar la descentralización del actual sistema de innovación, optimizando las decisiones de inversión pública e incorporando a las regiones chilenas a un desarrollo más equitativo y a redes europeas de innovación por medio de la asesoría de expertos en la metodología europea RIS o Estrategias Regionales de Innovación.</p>

<p>
La Región de Coquimbo es una de las 7 regiones del país que fueron seleccionadas para participar de esta iniciativa, la cual se encuentra en pleno desarrollo: con los equipos que comandan este proyecto (Directorio Regional de Innovación y Equipo de Gestión) conformados y con una metodología de trabajo definida, a través de la cual se espera obtener como resultado la Estrategia Regional de Innovación.
</p>

<h3>Con el proyecto RED también se busca:</h3>


<ol>
  <li>Profesionalizar la gestión de las decisiones e inversiones en políticas de innovación regional, con el apoyo de expertos europeos.</li>
  <li>Establecer y diseñar las Estrategias Regionales de Innovación para las siete regiones participantes. </li>
  <li>Construir una red entre regiones europeo-chilenas que dinamice la información, las decisiones y las oportunidades de innovación regional.</li>
</ol>
