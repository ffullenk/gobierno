          <h2>Identidad Regional</h2>
          <p>El presente libro, contiene una s&iacute;ntesis de lo m&aacute;s elevante del proyecto de investigaci&oacute;n: "Estudios para el fortalecimiento de la Identidad Regional de Coquimbo", realizado durante el 2009, por la Universidad de La Serena y financiado por la SUBDERE.</p>
<p>Reforzar la construcci&oacute;n de una Identidad Regional, constituye uno de los seis lineamientos de la Estrategia Regional de Desarrollo 2020.  As&iacute;, nuestro Gobierno Regional de Coquimbo, en conjunto con el Consejo Regional, se complace en presentar el libro denominado "Identidad en la Regi&oacute;n de Coquimbo", fruto del proyecto "Estudios para el Fortalecimiento de la Identidad Regional", en el cual, a trav&eacute;s del trabajo en veintitr&eacute;s localidades de la regi&oacute;n, se indaga y profundiza en el redescubrimiento de nuestra identidad regional.</p>
<p>Este esfuerzo mancomunado entre la Universidad de La Serena y su grupo de investigadores, nos permite recorrer nuestra historia desde sus ra&iacute;ces precolombinas hasta nuestros d&iacute;as; desplazarnos a trav&eacute;s de las tres cuencas de nuestro territorio: Elqui, Limar&iacute; y Choapa;  invit&aacute;ndonos a la reflexi&oacute;n para descubrir este imaginario colectivo denominado Regi&oacute;n de Coquimbo.</p>
<p>La Regi&oacute;n de Coquimbo es un territorio marcado por los valles transversales y la escasez de agua, en ella conviven diversas identidades con caracter&iacute;sticas propias, conservando algunas costumbres y tradiciones desde tiempos pret&eacute;ritos, mientras que otros rasgos se han modificado por factores econ&oacute;micos, culturales y sociales.</p>
<p>Las comunidades agr&iacute;colas, la criancer&iacute;a, la miner&iacute;a, la pesca y los bailes religiosos, conviven con las din&aacute;micas contempor&aacute;neas de las grandes urbes, influenciadas por las nuevas tecnolog&iacute;s y la globalizaci&oacute;n. Todo lo cual va actualizando, recreando y transformando las identidades.</p>
