<H2>Reglas aplicables:</H2>

<p>El Gobierno Regional de Coquimbo, viene en hacer presente que  el tratamiento de datos personales que se realiza en portal electr&oacute;nico www.gorecoquimbo.cl del Servicio Administrativo del Gobierno Regional de Coquimbo, se rige por las siguientes reglas:</p>
                      
<div>
    <ol>
        <li>El Gobierno Regional de Coquimbo asegura la confidencialidad de los datos personales de los usuarios(as) que se registren como tales en el sitio Web www.gorecoquimbo.cl mediante el o los formulario(s) establecido(s) para esos efectos. Sin perjuicio de sus facultades legales, Servicio Administrativo del Gobierno Regional de Coquimbo s&oacute;lo efectuar&aacute; tratamiento de datos personales respecto de aquellos que han sido entregados voluntariamente por los Usuarios(as) en el mencionado formulario.</li>
        
        <li>Los datos personales de los Usuarios(as) ser&aacute;n utilizados para el cumplimiento de los fines indicados en el formulario correspondiente y siempre dentro de la competencia y atribuciones del Gobierno Regional de Coquimbo.</li>
                          
        <li>El Gobierno Regional de Coquimbo podr&aacute; comunicar a otros organismos del Estado, los datos personales de sus usuarios(as), conforme lo establecido al efecto en la Ley 19.628 y sus modificaciones.</li>
                          
        <li>El Gobierno Regional de Coquimbo, en caso de ser requerido judicialmente al efecto, proceder&aacute; a comunicar los datos personales de los usuarios(as) que le sean solicitados.</li>
        
        <li>El Gobierno Regional de Coquimbo podr&aacute; comunicar a terceros informaci&oacute;n estad&iacute;stica elaborada a partir de los datos personales de sus usuarios(as), sin el consentimiento expreso del titular, cuando de dichos datos no sea posible identificar individualmente a los titulares, de conformidad a la Ley</li>
    </ol>
</div>
                      
<H2>Derechos del Usuario(a):</H2>
    
    <p>El Usuario(a) podr&aacute; en todo momento ejercer los derechos otorgados por la Ley N&ordm; 19.628 y sus modificaciones posteriores. En espec&iacute;fico, podr&aacute;:</p>

<div>
    <ul>
      <li>Solicitar informaci&oacute;n respecto de los bancos de datos de que sea responsable el organismo, el fundamento jur&iacute;dico de su existencia, su finalidad, tipos de datos almacenados y descripci&oacute;n del universo de personas que comprende;</li>
      
      <li>Solicitar informaci&oacute;n sobre los datos relativos a su persona, su procedencia y destinatario, el prop&oacute;sito del almacenamiento y la individualizaci&oacute;n de las personas u organismos a los cuales sus datos son transmitidos regularmente;</li>
      
      <li>Solicitar se modifiquen sus datos personales cuando ellos no sean correctos o est&eacute;n desactualizados, si fuere procedente;</li>
      
      <li>Solicitar la eliminaci&oacute;n o cancelaci&oacute;n de los datos entregados cuando as&iacute; lo desee, en tanto fuere procedente;</li>
      
      <li>Solicitar, de conformidad con lo dispuesto en la Ley 19.628, copia del registro alterado en la parte pertinente, si fuere del caso; y,</li>
      
      <li>Oponerse a que sus datos personales sean utilizados para fines estad&iacute;sticos.</li>
    </ul>
</div>
                      
<p>Para ejercer sus derechos el Usuario(a) podr&aacute; dirigirse al Link Servicio Cont&aacute;ctenos de la P&aacute;gina Web www.gorecoquimbo.cl indicando claramente su solicitud.</p>
                      
