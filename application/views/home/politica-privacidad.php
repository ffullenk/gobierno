<H2>Introduccion:</H2>

<p>El Gobierno Regional de Coquimbo, conciente de la necesidad de de promover el respeto a de la intimidad y privacidad de los Usuarios(as) de Internet y conforme a lo dispuesto en el art&iacute;culo 19 N&ordm; 4 de la Constituci&oacute;n Pol&iacute;tica de la Rep&uacute;blica y a las normas pertinentes de la Ley N&ordm; 19.628 sobre protecci&oacute;n de la vida privada y sus modificaciones, y las normas internacionales que rigen la materia.</p>


<p>El Gobierno Regional de Coquimbo adhiere a las normas autorregulatorias que buscan proteger el derecho b&aacute;sico de las personas naturales y jur&iacute;dicas  a resguardar la confidencialidad de sus datos, y que cumplen &iacute;ntegramente con las instrucciones impartidas por los organismos especializados en tema inform&aacute;ticos.</p>

<H2>Representante Legal:</H2>

