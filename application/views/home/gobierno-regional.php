<h1>GOBIERNO REGIONAL </h1>
<hr />
<br/>
El Gobierno Regional es un servicio aut&oacute;nomo que tiene a su cargo la administraci&oacute;n de la regi&oacute;n, mediante la formulaci&oacute;n de pol&iacute;ticas de desarrollo que tomen en cuenta tanto los lineamientos nacionales como aquellos formulados desde cada uno de los municipios, que en el caso de la regi&oacute;n de Coquimbo son quince.
<p>Est&aacute; compuesto por el Intendente Regional en su calidad de &Oacute;rgano Ejecutivo y por el Consejo Regional, que est&aacute; formado por representantes de las tres provincias de la regi&oacute;n y que son electos por los concejales de cada una de las comunas. El Consejo Regional o CORE, define y sanciona las estrategias regionales de desarrollo y sus modificaciones, las iniciativas de inversi&oacute;n en la regi&oacute;n, a trav&eacute;s de la asignaci&oacute;n de recursos provenientes del Fondo Nacional de Desarrollo Regional.</p>
<h3>COMPOSICI&Oacute;N DEL GOBIERNO REGIONAL</h3>
<p> El Gobierno Regional est&aacute; constituido por: </p>
          <ul>
            <li>El Intendente, quien preside el Gobierno Regional y que es designado directamente por el Presidente de la Rep&uacute;blica para ejercer el cargo y ser su representante en la regi&oacute;n. Adem&aacute;s, el Intendente , encabeza el Consejo Regional, compuesto por representantes de las tres provincias que integran la Regi&oacute;n de Coquimbo. </li>
            <li>El Consejo Regional que es un organismo colegiado que representa a las tres provincias de la regi&oacute;n de Coquimbo como &Oacute;rgano de fiscalizaci&oacute;n y participaci&oacute;n de la comunidad. </li>
            <li>En el Gobierno Regional la iniciativa para presentar propuestas reside en el Intendente. Al Consejo le corresponde aprobar, modificar o sustituir dichos planes. Luego, el Intendente debe ejecutar lo aprobado por el Consejo, que, a su vez, a &eacute;ste le corresponde fiscalizar el desempe&ntilde;o del Jefe Regional, quien debe dar una cuenta de su gesti&oacute;n al Consejo al menos una vez al a&ntilde;o, presentando el balance de la ejecuci&oacute;n presupuestaria y el estado de la situaci&oacute;n financiera.</li>
          </ul>

 


