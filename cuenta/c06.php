<h1>Gobierno en plena  marcha</h1>
<p>Hoy, rindo la Cuenta P&uacute;blica de lo que hemos hecho durante  el  a&ntilde;o 2007. Es la tercera desde el inicio de mi gesti&oacute;n. Lo hago en virtud de lo  que establece el art&iacute;culo 26 de la Ley Org&aacute;nica de Gobierno y Administraci&oacute;n Regional.</p>

<p>Hemos elegido hacerlo en la comuna de R&iacute;o Hurtado,  un espacio rural, con ello estamos dando una muestra no solo de descentralizaci&oacute;n, sino adem&aacute;s que  la Regi&oacute;n de Coquimbo somos todos  y todas, los que  habitamos en la ciudad  y en el campo, somos todas personas en igualdad de derechos y en dignidad.</p>

<p>El 2007 fue un a&ntilde;o de trabajo intenso. Nos hemos dedicado a trabajar, todo el equipo del Gobierno de la Regi&oacute;n. Hemos ya instalado  un estilo de gesti&oacute;n regional, cercano a la gente, orientado a resultados, pero por sobretodo pensando que las personas son  sujetos de nuestra atenci&oacute;n.</p>

<p>Estamos convencidos que la participaci&oacute;n social  es un valor fundamental para el ejercicio de la democracia.</p>

<p>Quiero agradecer en nombre de la Presidenta  de la Rep&uacute;blica,  a la comunidad, a los Consejeros  Regionales, a  todo el equipo de gobierno, a todos   y  todas e invitarlos  a construir m&aacute;s ciudadan&iacute;a,  a hacer uso de los derechos y ser responsable en los deberes, a seguir trabajando en esta regi&oacute;n de oportunidades, porque somos la Regi&oacute;n Estrella.</p>




