			 <tr><td><p class="noticia-e2" align="justify"><strong>INVERSIÓN EN CAPITAL HUMANO</strong><br>
“Construimos región”, con personas como Don Julio Jarpa, que hoy nos acompaña desde la Parte Alta de Coquimbo. Es gracias al trabajo de dirigentes como Usted, que el gobierno a través del programa Chile Barrio, ha permitido que más de 1200 familias de la región dejaran de vivir en 17 asentamientos. Hoy cuentan con subsidio, mejoramiento de viviendas, títulos de dominio, sedes sociales y canchas deportivas. Por otra parte, cerca de cinco mil 400 familias recibieron soluciones habitacionales, mejorando con ello significativamente su calidad de vida.</p>
<p class="noticia-e2" align="justify">
Y porque el emprendimiento, la innovación y la integración a los nuevos mercados son un sello de nuestra gestión, es que a través de una experiencia piloto de fondos concursables, 123 microempresarios consiguieron impulsar sus ideas de negocio. Además, dar mayor equidad a la intervención pública ha sido otro objetivo. Por ejemplo, el programa piloto Proempresaria permitió que 41 mujeres fortalecieran sus proyectos de emprendimiento y, por otra parte, 380 mujeres campesinas hicieron realidad sus sueños en el ámbito agrícola. Esto significa que muchos hogares hoy cuentan con una mejor fuente de ingresos.</p>
<p class="noticia-e2" align="justify">
No resulta extraño entonces, el que nuestra región haya sido elegida como sede para la realización del encuentro de mujeres líderes del Foro de Cooperación Económica del Asia Pacífico, APEC. Además de ser un desafío, significa una serie de oportunidades para nuestro progreso. Es con las mujeres de nuestra región que empezamos a mirar más decididamente hacia el Asia Pacífico.</p>
<p class="noticia-e2" align="justify">
Además, durante el presente año 25 empresarios regionales apoyados por el Gobierno salieron a la búsqueda de mercado y de transferencias tecnológicas en los países de Brasil, Japón, España, Bélgica y Francia; aprovechando así la ruta abierta hacia la internacionalización de nuestra economía que nos han dejado los acuerdos comerciales internacionales firmados por el Gobierno de Chile.</p>
<p class="noticia-e2" align="justify">
Esa es la verdadera visión de Estado que nos mueve y por la que trabajamos a diario, porque no es en los grandes anuncios que se agota nuestra labor, sino en la sumatoria de iniciativas y espacios abiertos para el diálogo y la concreción de las grandes ideas.</p>
<p class="noticia-e2" align="justify">
Y porque la calidad de vida no pasa sólo por el alimento para el cuerpo, es que estamos nutriendo poco a poco nuestro espíritu.  Por eso la creación del Consejo Nacional de la Cultura y las Artes marca un hito importante en lo desarrollado durante el año pasado.  En nuestra región, la figura de Gabriela Mistral y de Jorge Peña Hen, dejaron huella, y es precisamente esa identidad la que queremos rescatar.  Hoy nos acompaña la orquesta sinfónica juvenil que fuera creada por el maestro Jorge Peña Hen, heredera de su legado que se multiplicó por todo Chile.</p>
<p class="noticia-e2" align="justify">
Su presencia con nosotros este mediodía, sella el vínculo de este Gobierno con los artistas locales y su comunidad.</p>
