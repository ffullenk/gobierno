			 <tr><td><p class="noticia-e2" align="justify"><strong>ENLACE Y CONECTIVIDAD</strong><br>
Desde ya estamos preparando nuestras ciudades y haciendo de ellas territorios más amables y mejor conectados.  Trabajando con la comunidad y gracias a la participación de los propios vecinos a través del Programa de Pavimentación Participativa, conseguimos pavimentar más de 2.700 metros lineales de calles y otros 50 kilómetros de carreteras, sólo durante el último año, porque entendemos que la infraestructura debe ser modernización e integración.</p>
<p class="noticia-e2" align="justify">
Por eso es que estamos trabajando en el próximo inicio de las obras de la doble vía en la ruta 5, desde la salida norte de La Serena hasta Bahía Inglesa, la que nos unirá físicamente de forma más segura con la región de Atacama.</p>
<p class="noticia-e2" align="justify">
Y porque la unión no es sólo física, es que estamos avanzando en la implementación del proyecto de la ruta 5 digital, que proveerá de mayor cobertura y calidad en la conectividad de internet al sector público de la Región, lo que se traduce por ejemplo, en un mayor número de escuelas y consultorios con acceso a internet de mejor calidad.</p>
<p class="noticia-e2" align="justify">
Y así como desde los colegios la comunidad puede conectarse al mundo, otra forma de acercar la tecnología a las personas, es a través de los centros de información juvenil.  Esta inversión de fondos regionales, permitirá durante este año la habilitación de otros nueve infocentros en igual número de comunas de las tres provincias, lugares desde donde las personas que quieran conectarse a Internet podrán hacerlo. Con esto, llegaremos a disponer de 93 puntos de conexión gratuita a internet abiertos a la comunidad en toda la región. Quiero mencionar aquí a don Luis Aracena, de La Serena, que gracias al programa de alfabetización digital para el adulto mayor, hoy se conecta a la red para estar en contacto con sus hijos en Venezuela y nos da un ejemplo de actividad y de cómo es posible adaptarse a las nuevas tecnologías.</p>
