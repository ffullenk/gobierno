			 <tr><td><p class="noticia-e2" align="justify"><strong>CIERRE</strong><br>
Estoy contento con lo que estamos haciendo.</p>
<p class="noticia-e2" align="justify">
Hemos avanzado en una colaboración público-privada, un trabajo entre el gobierno y la comunidad. Hay espacios de diálogo y colaboración.</p>
<p class="noticia-e2" align="justify">
Hoy podemos decir que nuestra apuesta para el desarrollo de la región se sustenta en tres pilares productivos: queremos ser los líderes en nuestro país en las áreas agrícola, acuícola y turística.</p>
<p class="noticia-e2" align="justify">
Para esto hemos avanzado en incrementar la investigación, la ciencia y la tecnología. Pero nos queda todavía dar un paso en el uso del conocimiento científico para llevar adelante nuestras ideas, nuestros emprendimientos.</p>
<p class="noticia-e2" align="justify">
Contamos con una infraestructura física y digital en crecimiento con nuevos proyectos desafiantes y trascendentales para enfrentar el futuro.</p>
<p class="noticia-e2" align="justify">
Hemos avanzado y nos queda aún una tarea importante en la integración social y la equidad.  Tenemos metas en salud, en educación, y en seguridad.</p>
<p class="noticia-e2" align="justify">
Contamos con un capital humano fundamental, el acceso a la capacitación y al emprendimiento, la incorporación eficaz de las mujeres y los jóvenes a la construcción de nuestra región se transforma en nuestra apuesta de Gobierno para el futuro.</p>
<p class="noticia-e2" align="justify">
Seguiré poniendo énfasis en los esfuerzos por acercar cada vez más el Gobierno a cada uno de los ciudadanos de nuestra región.</p>
<p class="noticia-e2" align="justify">
No me queda más que agradecer a todas las personas que nos han colaborado y al empeño que están poniendo para construir esta región. Un agradecimiento especial a cada uno de los funcionarios públicos, sin los cuales no habríamos podido hacer realidad esta cuenta.</p>
<p class="noticia-e2" align="justify">
Termino diciendo que existe hoy una gran oportunidad en la región de seguir dando saltos en el desarrollo.  </p>
<p class="noticia-e2" align="justify">
Debemos seguir trabajando por una mejor calidad de vida y perspectivas de futuro, pero, por sobre todo, exijo una mayor dignidad y equidad con las personas más pobres de nuestra región.</p>
<p class="noticia-e2" align="justify">
De esta forma estamos construyendo una región más justa, más solidaria y con mayor progreso para cada uno de nuestros ciudadanos,</p>
<p class="noticia-e2" align="justify">
Gracias por acompañarnos.</p>
