			 <tr><td><p class="noticia-e2" align="justify"><strong>INFRAESTRUCTURA</strong><br>
Y tal como dejamos entrar los avances científicos, se convierte en un imperativo el abrir la puerta de salida al extranjero de nuestros productos. Por eso es necesario generar una infraestructura portuaria – tanto marítima como aérea - acorde a los nuevos desafíos, a estos nuevos tiempos.  Hoy quiero pedirle a los representantes regionales en el Congreso, nuestros seis diputados y dos senadores, que nos apoyen para facilitar y atraer inversiones en nuestro puerto de Coquimbo, como por ejemplo a través de modificar la Ley de Presupuesto de la Nación para que permita la inversión pública en nuestros puertos; así como también les solicitamos el apoyo para la próxima construcción del aeropuerto regional Gabriela Mistral.</p>
<p class="noticia-e2" align="justify">
Ambas iniciativas, aseguran una infraestructura básica para la integración de nuestra región al mundo.</p>
<p class="noticia-e2" align="justify">
Hoy, ya nueve regiones del país cuentan con aeropuertos aptos para su desarrollo. ¿Por qué nosotros nos vamos a quedar atrás?  ¿Por qué no aprovechar las oportunidades de crecimiento que se nos presentan?  Así lo entendemos como Gobierno y por eso hemos trabajado para que este proyecto sea licitado durante este año.</p>
<p class="noticia-e2" align="justify">
El paso fronterizo Agua Negra, es otra iniciativa que ha estado en el centro de las preocupaciones de este Gobierno Regional.  Además de los avances en su pavimentación, donde se agregaron 30 nuevos kilómetros de pavimento a la ruta, nuestra integración con San Juan, Argentina, va más allá de un hito vial. En noviembre del año pasado, una delegación de 120 personas, entre autoridades, empresarios, académicos y otras fuerzas vivas de la Región, asistimos al cambio de mando del gobernador provincial, y en el último año hemos realizado dos reuniones del Comité de Frontera, incorporando efectivamente al mundo de la cultura y los trabajadores al debate sobre la integración.</p>
<p class="noticia-e2" align="justify">
En los próximos días viajaremos nuevamente a San Juan para conocer los resultados del estudio de prefactibilidad para la construcción del túnel que abrirá definitivamente el Paso Agua Negra. Más allá de los montos que deberemos invertir, estamos convencidos de que el túnel representa un avance para nuestra región y el país, por lo cual solicitaré que paralelamente iniciemos un estudio de evaluación social y privada en conjunto con nuestros hermanos argentinos para ir buscando desde ya vías de financiamiento a esta gran iniciativa y consolidar definitivamente nuestra integración, paso fundamental para nuestro desarrollo.</p>
<p class="noticia-e2" align="justify">
Consolidar este paso fronterizo, significa nuestra integración con el resto del cono sur, gracias a la materialización de un corredor bioceánico que nos permita proyectar nuestros productos, el turismo y nuestra actividad científica y académica.</p>
<p class="noticia-e2" align="justify">
Y no sólo integración internacional, sino también con nuestros vecinos de la región de Valparaíso. Con ellos, realizamos una primera jornada de trabajo que consideró temas de infraestructura vial, salud, cooperación turística, agroexportación y áreas de manejo.  Además de compartir la experiencia de implementación de la nueva justicia en ambos territorios.</p>
<p class="noticia-e2" align="justify">
Y en cuanto a integración de las zonas rurales de nuestra región, hemos mejorado las condiciones de vida de 14 localidades en lo que respecta al acceso a agua potable.</p>
<p class="noticia-e2" align="justify">
En este sentido, estamos trabajando con la Dirección de Presupuestos del Ministerio de Hacienda para que la región reciba la mayor parte de los recursos derivados de la concesión de la sanitaria ESSCO, en proyectos de agua potable rural, por un monto cercano a los 16 mil millones en los próximos tres años.  Esto nos permitiría que gran parte de las localidades rurales de nuestra región cuenten con acceso al agua potable, y lo que es una novedad, poder avanzar con plantas de tratamiento que mantengan la calidad de nuestras aguas.</p>
<p class="noticia-e2" align="justify">
El proyecto se comenzará a implementar durante el segundo semestre de este año, con una serie de programas graduales que nos convertirán en la región piloto de la nueva política de agua potable rural que impulsará el Gobierno en el país.</p>
<p class="noticia-e2" align="justify">
Por el agua potable también fluye nuestro desarrollo. Somos la única región que ha presentado una apuesta estratégica para utilizar los recursos de compensación por la concesión de las sanitarias.</p>
<p class="noticia-e2" align="justify">
Si queremos avanzar en calidad de vida y en oportunidades productivas, certificar la calidad de nuestras aguas, es un requisito fundamental.</p>
<p class="noticia-e2" align="justify">
Aquí vamos a dar un gran salto, queremos liderar en Chile el saneamiento de las aguas.  Queremos ser una región que no contamina, una región con aguas limpias y claras.</p>
