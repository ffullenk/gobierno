			 <tr><td><p class="noticia-e2" align="justify"><strong>INTEGRACIÓN SOCIAL</strong><br>
Porque queremos que cada día más chilenas y chilenos se integren a la construcción de una comunidad para todos, el sistema Chile Solidario, ya consiguió que más de 900 familias de nuestra región, cuenten con ingresos superiores a la línea de indigencia, mientras que otras dos mil 500 están recibiendo el aporte solidario que les permitirá una mayor integración a una vida digna, además de prioridad a la hora de recibir pensiones asistenciales, educación, salud y vivienda de calidad.</p>
<p class="noticia-e2" align="justify">
Es el caso de don Guillermo Díaz. Él, junto a otras familias del Sector El Triángulo, ha cumplido satisfactoriamente con las exigencias a las que se comprometió al ingresar al Sistema. Gracias a su iniciativa y al apoyo del Gobierno, don Guillermo ha podido implementar un almacén de abarrotes y artículos de librería, cumpliendo con todos los permisos legales e incorporando para su familia un ingreso más estable.</p>
<p class="noticia-e2" align="justify">
Tal como don Guillermo, desde la reciente aprobación de la ley del Sistema Chile Solidario en el Congreso, las personas más vulnerables de nuestro país y de nuestra región contarán con un sistema de protección social permanente, que trascenderá la gestión del gobierno del Presidente Lagos.</p>
<p class="noticia-e2" align="justify">
Chile Solidario se ha transformado para más de 4 mil familias de extrema pobreza de la región, en un llamado a la esperanza, en un Gobierno que cree en ellos y en sus capacidades para salir adelante. </p>
<p class="noticia-e2" align="justify">
Otro aspecto fundamental para avanzar en la integración y equidad social es la salud de las personas. </p>
<p class="noticia-e2" align="justify">
Yo, como Intendente, no quiero dejar pasar este momento para decir que agradezco el trabajo de todos los profesionales y la gente que trabaja en la atención de salud.</p>
<p class="noticia-e2" align="justify">
Pero nuestro desafío es también que nuestros habitantes sientan y reciban una atención personalizada, humana y de calidad cuando más lo necesitan.</p>
<p class="noticia-e2" align="justify">
Yo quiero interpelar a todos los profesionales de salud (de los municipios, médicos, sector público, sector privado, funcionarios de gobierno) para que resolvamos juntos este tema.  Los llamo a no dejarse llevar por un espíritu de frustración sobre esto, y a mantener un animo de resolución de que este es un problema que vamos a enfrentar y superar porque la dignidad de las personas es el principal bien que debemos cautelar.</p>
<p class="noticia-e2" align="justify">
El calendario de inversiones necesarias en los hospitales de la Región y el inicio del programa de excelencia hospitalaria en Coquimbo, La Serena y Ovalle, es una muestra de esto y de lo activo que se encuentra el sector salud en nuestra región, que gracias a la reforma en curso, ha permitido ya dar una atención a 722 personas en las patologías cubiertas por la implementación del plan auge piloto desde agosto del 2002.</p>
<p class="noticia-e2" align="justify">
Estas garantías, que se suman a las que ya estaban y explicitan el compromiso básico del sistema público de salud, representan la mayor revolución para el sector desde la creación del servicio nacional de salud.</p>
<p class="noticia-e2" align="justify">
Entregar una mejor atención a las personas es un imperativo moral para nosotros, como Gobierno, y no vamos a dudar ni dar marcha atrás.</p>
<p class="noticia-e2" align="justify">
Porque al conocer casos como el de Damaris Adaros, que está ahora con nosotros, no podemos si no comprometernos más con esta reforma.  Damaris tiene dieciséis años y gracias a la incorporación del tratamiento de cardiopatías congénitas al auge, ella hoy puede hacer una vida normal y asistir al Liceo de Tierras Blancas.  Y porque sabemos que hay muchos más como Damaris, es que vamos a seguir luchando por la reforma, porque Chile la quiere y se la merece.</p>
<p class="noticia-e2" align="justify">
Tenemos que poner más donde hay menos.  Esa es nuestra premisa a todo nivel de acciones, pero esto para nosotros está muy relacionado con la instalación de capacidades.</p>
<p class="noticia-e2" align="justify">
Por eso es que el Gobierno Regional comprometió la integración de 45 nuevos colegios, escuelas y liceos de la región a la Jornada Escolar Completa. Esto permitirá que hasta el 2005, unos 26 mil estudiantes mejoren las condiciones en las que están desarrollando sus estudios. Y seguiremos con nuevos proyectos.</p>
<p class="noticia-e2" align="justify">
Por otra parte, el proyecto de la red Enlaces, que conecta las escuelas a Internet, permitió que otros dos mil 700 niños, niñas y jóvenes en más de 50 establecimientos rurales y urbanos, aprovechen todas las posibilidades de las tecnologías de la información y la comunicación. Y junto a ellos, también sus familias que pueden acceder a los colegios fuera del horario de clases y usar esta infraestructura para comunicarse con el mundo.</p>
<p class="noticia-e2" align="justify">
Pero necesitamos que el proceso de educación sea permanente.  Por eso es que, en conjunto con nuestros vecinos de la región de Atacama, ya estamos trabajando en las áreas de acuicultura y agroindustria, dentro del programa de capacitación permanente Chile Califica. Esto permitió que 630 habitantes de la región de Coquimbo pudieran nivelar sus estudios básicos y medios.</p>
<p class="noticia-e2" align="justify">
Por eso hoy nos acompaña la señora Rosa Covarrubias, que aprovechó la oportunidad y obtuvo su licencia de enseñanza media.  Rosa tiene tres hijos. Por la educación de ellos postergó sus estudios. Hoy, cuando los tres son ya profesionales: el primero ingeniero aeronáutico, la segunda profesora de biología y la menor ingeniero laboratorista, </p>
<p class="noticia-e2" align="justify">
Usted Sra. Rosa ha decidido crecer culturalmente, dándose cuenta que nunca es tarde para aprender.</p>
<p class="noticia-e2" align="justify">
Rosa es un ejemplo perfecto de la forma en que ponemos en práctica nuestro concepto de igualdad y participación.  Ella, que tantas veces se sintió excluida por no saber de los temas que otras personas hablaban, hoy siente que tiene las mismas posibilidades que el resto para desenvolverse en nuestra sociedad.  Por casos como el de ella es que nos jugamos para instalar capacidades en cada mujer y hombre de nuestra región de Coquimbo, para que puedan emplearlas en mejorar su calidad de vida.</p>
<p class="noticia-e2" align="justify">
De todas maneras, al igual que todo el país, no estoy satisfecho con los resultados que hasta ahora han mostrado las pruebas de medición de calidad en educación. </p>
<p class="noticia-e2" align="justify">
Sin embargo, sí estoy contento con el ánimo de los jóvenes por aprender, de los padres por integrarse al proceso educativo de sus hijos, y por la apertura de los profesores al proceso de transformación de la educación.  Sumado a esto, el gobierno ha hecho un esfuerzo importante para mejorar la calidad de la infraestructura de las escuelas permitiendo la implementación de la jornada escolar completa.</p>
<p class="noticia-e2" align="justify">
Aquí tenemos que dar saltos importantes. Un compromiso que debemos asumir todos: estudiantes, padres, madres, profesores, directores, empresarios y toda la comunidad.</p>
<p class="noticia-e2" align="justify">
Porque estamos formando personas, necesitamos que todos hagamos un gran esfuerzo por ser pioneros en la calidad de la educación. Miren este nuevo colegio, esta construcción que nos pone al mejor nivel.</p>
<p class="noticia-e2" align="justify">
Los resultados tienen que estar a la altura del siglo XXI. Nuestra región esta disponible para hacer todo lo necesario para avanzar en esto.</p>
<p class="noticia-e2" align="justify">
En la consolidación de una vida sana, Chiledeportes consiguió que cerca de 36 mil personas participaran de algún programa de actividad física, además de permitir que en ocho comunas de las tres provincias de la región, se pudieran reparar y habilitar recintos deportivos.</p>
<p class="noticia-e2" align="justify">
Claro que ninguno de estos recintos serviría, sin el trabajo desinteresado de personas como Juan Carlos Núñez, que es el presidente de la Asociación de Academias de Fútbol de La Serena, y es parte de la organización del campeonato regional de la asociación, en el que se terminan involucrando cerca de 2 mil 500 familias todos los años.  Juan Carlos es uno de los que nos ayuda a construir una región más sana, a través del trabajo en equipo.</p>
<p class="noticia-e2" align="justify">
Con el apoyo de Conace, 215 personas más que el año anterior, recibieron tratamientos de rehabilitación de drogas, tal como lo hizo Cecilia Pizarro, de Tierras Blancas.  Cecilia es parte del centro Lauka, hasta donde llegó para tratar su adicción a las drogas, hace poco menos de un año.  Hoy, Cecilia lleva ocho meses de abstinencia y ha podido disfrutar de verdad el tiempo que pasa con sus dos hijos y su esposo, porque tuvo la voluntad y el apoyo para hacerlo.  Por eso, este año iniciaremos en la región una experiencia piloto con la creación del primer centro de tratamiento y rehabilitación público del país, que contará con una disponibilidad de 20 camas para una atención personalizada.</p>
<p class="noticia-e2" align="justify">
Además, gracias al apoyo del Consejo Regional, este año seguimos con programas de prevención como los que se realizan en colegios de toda la región, que nos permitieron que más de 115 mil niños y jóvenes recibieran información preventiva.</p>
<p class="noticia-e2" align="justify">
Con estas acciones, estamos velando también por la seguridad de la comunidad.  Precisamente en este aspecto, seguimos avanzando en la difusión de la nueva justicia penal.  Durante el año pasado y los cuatro primeros meses de este año, explicamos la reforma a cerca de 3 mil habitantes de nuestra región, logro reflejado en múltiples encuestas de opinión hechas por empresas privadas, y que nos ha traído una buena evaluación ciudadana de la Reforma y un alza en la tasa de denuncias a nivel regional, a la que estamos respondiendo mediante la coordinación adecuada entre las policías y el Ministerio Público. </p>
<p class="noticia-e2" align="justify">
Seremos inclaudicables en la aplicación de la ley para combatir la delincuencia, usando todos los medios legales existentes. Más aún hoy, con la nueva ley de control de identidad, que facilitará la labor de carabineros en los procesos de detención.</p>
<p class="noticia-e2" align="justify">
Además, el contingente de carabineros crecerá en la región con la construcción de la nueva comisaría de Las Compañías, al tiempo que la construcción del nuevo recinto penitenciario regional, permitirá reubicar a 1.600 reos en un espacio más digno y justo, respondiendo a una demanda social para todos estos hermanos chilenos que han caído en la delincuencia.</p>
<p class="noticia-e2" align="justify">
Sin embargo, el combate a este flagelo, es algo que nos involucra a todos, por lo que es fundamental la participación de la comunidad organizada.</p>
<p class="noticia-e2" align="justify">
Dentro de las denuncias de delitos con connotación social, se encuentran los casos de violencia intrafamiliar.  El aumento de este tipo de denuncias, nos muestra que la violencia en los hogares es un tema que está dejando de ser oculto.  De ahí la importancia de programas como el de prevención y atención a las víctimas de violencia intrafamiliar, porque cuidar a todos los miembros de la familia es tarea de todos, y las 1.200 personas atendidas, son prueba de este compromiso.</p>
<p class="noticia-e2" align="justify">
Creo que en el tema de seguridad, estamos abriendo caminos.  Pero sin duda, que el camino más importante es el empoderamiento de la comunidad.  Los barrios más seguros se hacen trabajando con las personas, fomentando el conocimiento mutuo de los vecinos y el uso de los espacios públicos.  La falta de seguridad es parte de la modernidad; no estamos frente a una crisis en nuestra región.  La seguridad es distinta de acuerdo a los tiempos y aquí tiene que haber un rol protagonista de la ciudadanía.</p>

