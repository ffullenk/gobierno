<h1>Cosechando Frutos</h1>

<p>Es la cuarta oportunidad en que me corresponde rendir esta cuenta pública. He intentado en estos años, profundizar también el proceso de descentralización al interior de la región, así hemos realizado la cuenta pública en la provincia de Elqui, el año pasado lo hicimos en Limarí y hoy en Choapa, aquí donde estamos prontos a concluir esta gran obra de riego y de desarrollo  como lo es el Embalse El Bato, compromiso de la Presidenta Bachelet, que estamos seguros cambiará el rostro a la provincia, hará producir  sus tierras, que ahora tendrán seguridad de riego  y que traerá más prosperidad a su gente.</p>

<p>Éste  es  un año particular, no podemos abstraernos de lo que está ocurriendo en el mundo, en nuestro país  y en la región. Estamos enfrentando una gran crisis, la más grande de los últimos 60 años,  que nos  ha golpeado con fuerza y con toda probabilidad seguirá haciéndose sentir. Estamos preocupados como Gobierno, son momentos difíciles que requieren más que nunca de mucha unidad, también de mucha solidaridad, de pensar en aquellos a quienes la crisis los golpea con más fuerza, pero por sobretodo de ser muy eficientes en nuestra gestión.</p>

<p>En  la región, hemos impulsado un conjunto de medidas, quiero destacar en esta oportunidad el Plan Extraordinario de Empleo que cuenta con recursos adicionales, por un monto de más de 25 mil millones de pesos adicionales y la creación de más de 30.000 empleos.</p>

<p>Al inicio de mi gestión, hice un símil con los ciclos productivos. Hoy puedo decir con orgullo que estamos recogiendo los frutos, que aún nos queda por cosechar  y sobretodo seguir sembrando, ya que ello es una labor que no termina. Lo hemos hecho intentando incorporar a todos  y todas, hemos escuchado, hemos conformado muchas comisiones y mesas de trabajo en aras de lograr un trabajo más participativo y coordinado.</p>

<p>Esta es también una invitación a todos y todas, a ser parte del sueño de  Construir una Región, más inclusiva social y territorialmente, con respeto al  medio-ambiente, con  resguardo de nuestro patrimonio cultural, que valorice la figura de nuestra poetisa Gabriela Mistral, que desarrolle sus potencialidades productivas. En suma una región  con mejor Calidad de Vida para las personas.</p>


