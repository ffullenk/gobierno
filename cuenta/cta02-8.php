			 <tr><td><p class="noticia-e2" align="justify"><strong>ACERCANDO EL GOBIERNO A LAS PERSONAS</strong><br>
Otra apuesta fundamental de nuestra gestión, es acercar el Gobierno a cada uno de los ciudadanos.  Precisamente porque sabemos que hay un déficit todavía presente en la atención a los usuarios de los servicios públicos, se ha hecho un esfuerzo para conseguir que todos los servicios hoy cuenten con oficinas de informaciones, reclamos y sugerencias, con la finalidad de poder escuchar mejor a las personas.</p>
<p class="noticia-e2" align="justify">
Por otra parte, durante el desarrollo de uno de los talleres mensuales de trabajo del Gabinete Regional, un grupo de jóvenes nos hablaba de sus anhelos, sus aspiraciones y de lo que ellos esperaban del Gobierno, de cómo nosotros no estábamos aprovechando su potencial.</p>
<p class="noticia-e2" align="justify">
Por esto se han realizado quince cabildos comunales de la juventud. Queremos recoger y asumir este déficit que tenemos como gobierno, superarlo y dar el salto necesario en nuestra región.  Desde ya contamos con el compromiso de más de 20 servicios públicos regionales para focalizar y adaptar su oferta mediante planes especiales relacionados con la juventud a través de cien iniciativas en áreas de cultura, participación, educación, trabajo, salud y prevención. Esto nos convierte en la primera región en el país que contará con un Plan Regional de Juventud elaborado en conjunto con ellos.</p>
<p class="noticia-e2" align="justify">
Al mismo tiempo, a través del programa de voluntariado, el año pasado se ejecutaron 45 proyectos de organizaciones sociales, que significaron poner en acción a más de mil personas. Este año, y haciéndonos cargo del llamado de los jóvenes, se constituirá una Red de Voluntariado Juvenil, que permitirá la participación efectiva de 400 jóvenes a través de más de 10 organizaciones, que tendrán un espacio físico habilitado para su funcionamiento.</p>
<p class="noticia-e2" align="justify">
Lo ocurrido el jueves pasado en Diaguitas, nos recordó no sólo los riesgos de ocupar los espacios que la naturaleza necesita para su devenir habitual, sino también la importancia de la solidaridad y el trabajo desinteresado de las personas.  Hoy quiero reconocer en Eduardo Rivera, miembro del centro de voluntariado SUMA de la Universidad de La Serena, a todos aquellos que han ayudado a las familias afectadas en esta localidad del valle de Elqui.  Personas como el carabinero Jimmy Cruz, que contribuyó para que hoy no estemos lamentando ninguna muerte en esta tragedia.</p>
<p class="noticia-e2" align="justify">
También quiero destacar el apoyo del Ejército que con un contingente de 40 soldados, nos están colaborando en limpieza de casas y quebradas.</p>
<p class="noticia-e2" align="justify">
Como Gobierno hemos actuado con la mayor agilidad frente a esta emergencia, disponiéndose en forma inmediata de maquinarias para el despeje de caminos y de 15 viviendas que estuvieron en dos días en Diaguitas para responder por las casas destruidas.</p>
<p class="noticia-e2" align="justify">
Sin embargo, quiero aprovechar este acto ciudadano, para contar a toda la comunidad regional, que el Ministerio de Obras Públicas ya destinó con fondos de emergencia la entrega de 100 millones de pesos, para la limpieza de las quebradas cercanas a Diaguitas, tales como Cebollín, Arenales y Puyai.  Porque así es como el Gobierno cumple sus compromisos.</p>
<p class="noticia-e2" align="justify">
El 60 por ciento de respaldo ciudadano al Gobierno del Presidente Lagos, nos hace sentir cercanos a la gente.  Sin embargo, tenemos que dar un paso más.  Por eso, a partir de este año, hemos generado una dirección de correo electrónico para el contacto directo de la comunidad con el Intendente.  Quiero responder directamente a sus inquietudes, por eso, desde ya está disponible el correo electrónico <a href="mailto:felipedelrio@gorecoquimbo.cl">felipedelrio@gorecoquimbo.cl</a>.</p>
