			 <tr><td><p class="noticia-e2" align="justify"><strong>FOCOS ESTRATÉGICOS DE LA REGIÓN</strong><br>
Hoy, veo una región que como resultado de este clima de conversaciones y de colaboración, paso a paso va entendiendo con claridad lo que quiere ser en este mundo global.  Hemos identificado cuáles son los focos fundamentales de nuestro desarrollo y nos hemos ido comprometiendo con ellos. </p>
<p class="noticia-e2" align="justify">
Cada vez es más nítido para todos nosotros, que nuestra región tiene una vocación específica.  Nuestra región está llamada a ser el centro agrícola, acuícola, y turístico de Chile para el mundo.  Esto, sobre la base del emprendimiento, la innovación y la integración a nuevos mercados, porque tenemos un territorio con un potencial tremendo.</p>
<p class="noticia-e2" align="justify">
Y de eso puede dar testimonio don Floridor Solar, representante de la Sociedad Agrícola Agromilenio, que ha aprovechado los instrumentos ofrecidos por el Ministerio de Agricultura a través de Indap, y hoy produce y exporta más de 10 mil cajas de uva de mesa en la comuna de Monte Patria. Porque juntos hemos sabido desarrollar y mantener una infraestructura de riego durante estos años, que ha permitido prosperar a Agromilenio y otras empresas, y crear en este territorio una fuente de trabajo para muchas personas.</p>
<p class="noticia-e2" align="justify">
El trabajo desarrollado por las asociaciones de regantes de los tres valles transversales de nuestra región, ha sido fundamental en este desarrollo.  Ellos entienden la responsabilidad social de su labor, y por eso han asumido desafíos en forma responsable y clara.  El proceso de traspaso de las obras del embalse Puclaro a la Junta de Vigilancia del río Elqui, es una iniciativa pionera en el país que sabemos será exitosa, y hacia esa certeza avanza el trabajo que se realiza con los regantes del embalse Corrales, donde además el Gobierno está invirtiendo en el canal alimentador de este tranque, concluyéndose un proyecto que asegurará once mil nuevas hectáreas para riego. Del mismo modo, luego de más de 35 años de funcionamiento, se está en el proceso de traspaso del embalse Paloma, en el que por primera vez se ha estado llegando a un avenimiento con las organizaciones, vislumbrándose un pronto traspaso a los regantes.</p>
<p class="noticia-e2" align="justify">
Otros que han llegado lejos, que hablan de lo que somos como región de Coquimbo al resto del mundo, son nuestros productos acuícolas, que se han potenciado fuertemente durante los últimos tres años. Hoy, gracias a personas como Ivonne Etchepare, ya no sólo Francia recibe nuestros ostiones, sino que también Brasil, Alemania y Japón.</p>
<p class="noticia-e2" align="justify">
La producción regional de ostiones y abalones, que hoy nos posiciona como pioneros a nivel nacional y nos aporta una identidad productiva, ha aprovechado los acuerdos de libre comercio firmados por el Presidente Lagos, y se ha apoyado en los instrumentos de fomento productivo del Gobierno para facilitar su salida al mundo.  En este campo, el trabajo desarrollado en conjunto y con la cooperación de instituciones como la Universidad Católica del Norte, Fundación Chile y la Agencia de Cooperación Japonesa, han sido fundamentales.  La constancia y la unión entre la ciencia y el emprendimiento a lo largo de los años, habla de la confianza instalada en nuestra zona, donde el diálogo nos permite dar un salto importante en el desarrollo y calidad de vida.</p>
<p class="noticia-e2" align="justify">
La calidad de nuestras aguas, nuestras condiciones productivas privilegiadas, son reconocidas en el mundo entero.  Precisamente la autoridad sanitaria europea, durante el viaje realizado por una delegación regional conformada por agentes públicos y privados, tocó este punto como una de las ventajas claras de nuestra región frente a nuestros competidores.</p>
<p class="noticia-e2" align="justify">
Nuestro desafío es mantener, proteger y mejorar la actual pureza de nuestras aguas.  Aquí tenemos una clara ventaja para nuestro desarrollo.</p>
<p class="noticia-e2" align="justify">
Tenemos que ser reconocidos en el mundo como una región que no contamina.</p>
<p class="noticia-e2" align="justify">
Nuestra región es el centro turístico de los chilenos. Y esto se expresa en que el año pasado nos visitaran más de 750 mil chilenos y 50 mil extranjeros, duplicando la población residente de nuestra región. La diversificación de nuestra oferta, se grafica en personas como Andrea Castillo, de Andacollo, quien ha sido capacitada en atención turística y aporta con su liderazgo a consolidar nuevos circuitos de interés, como el observatorio Collowara, construido recientemente con recursos del Gobierno Regional en esa ciudad.</p>
<p class="noticia-e2" align="justify">
Esta nueva apuesta del Gobierno, cambia la mirada de todo un pueblo desde la tierra hacia el cielo, para que Andacollo pueda mirar más lejos.</p>

