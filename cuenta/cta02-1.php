			 <tr><td><p class="noticia-e2" align="justify"><strong>EJEMPLOS DE COLABORACI&Oacute;N P&Uacute;BLICO-PRIVADA</strong><br>
Para graficar la relación público-privada, me permito citar dos ejemplos concretos:</p>
<p class="noticia-e2" align="justify">
Por un lado, lo vivido con la empresa minera Pelambres.  En este caso, tuvimos un proceso de discusión largo, en que se equilibró de la mejor forma el progreso económico con la sustentabilidad ambiental de toda una provincia.  La acción del Gobierno enriqueció un proyecto para mejorar las condiciones de desarrollo de la comunidad involucrada, considerando paralelamente la exigencia de descarga cero a las aguas de la bahía de Los Vilos, con lo que garantizamos la calidad de las aguas de nuestra región.</p>
<p class="noticia-e2" align="justify">
Al final de la conversación vivida con la empresa, vemos cómo se están usando estándares más altos de lo que la ley exige y se colabora con las comunidades potencialmente afectadas, al tiempo que los habitantes aceptaron que no todas sus reivindicaciones podrían ser respondidas a cabalidad, para poder crear más empleos.</p>
<p class="noticia-e2" align="justify">
Otro claro ejemplo de esta relación, es la apuesta por la innovación y el impulso de nuevos productos en los que podamos ser pioneros. Con esfuerzos y recursos públicos y privados, estamos creando el primer laboratorio de certificación de aceites de oliva en Chile y el Cono Sur, así como también el primer panel de cata de este tipo de aceites. Este laboratorio estará ubicado en Ovalle, en la sede de la Universidad de La Serena.</p>
<p class="noticia-e2" align="justify">
A través de esta apuesta, el Instituto de Investigación Agropecuaria del gobierno aporta con su experiencia tecnológica y las empresas Agronoble y Cerrillos de Tamaya con sus 480 hectáreas de plantaciones de olivos y la construcción de una almazara que permitirá la elaboración de aceite de oliva con una capacidad de hasta 2 mil hectáreas de producción, que podrá acoger también la de los pequeños agricultores.</p>
<p class="noticia-e2" align="justify">
Gracias a esta colaboración, también contaremos con un mapa claro de los territorios aptos en la región para el incremento de la producción de aceite. No sólo seremos líderes en esta área, sino que gracias a la cooperación y los acuerdos comerciales, la región de Coquimbo será también líder en la producción con alta tecnología.</p>
<p class="noticia-e2" align="justify">
En este clima de confianza, estamos todos aprendiendo.  Aquí no hay nada escrito: estamos creando una nueva forma de entendernos y establecer confianzas, estamos avanzando.</p>
