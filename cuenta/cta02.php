			 <tr><td><p class="noticia-e2" align="justify"><strong>INTRODUCCI&Oacute;N</strong><br>
Amigos y amigas presentes, miembros de la comunidad de las quince comunas de la región de Coquimbo, dirigentes y dirigentas sociales, empresariales, estudiantiles; miembros del poder legislativo, del poder judicial, de las fuerzas armadas y de orden; autoridades eclesiásticas; representantes de las universidades e institutos profesionales de la región; alcaldes, alcaldesas, concejales y concejalas; consejeros y consejeras regionales, integrantes del gabinete regional, directores y directoras de servicios públicos, funcionarias y funcionarios públicos, queridos jóvenes que nos acompañan esta mañana.</p>
<p class="noticia-e2" align="justify">
En estos dos años en la administración regional, hemos hecho una apuesta fuerte para trabajar con las personas, en escuchar sus requerimientos y dar respuesta de la mejor forma posible.</p>
<p class="noticia-e2" align="justify">
Por eso hoy, quisimos entregar esta cuenta pública de gestión, con la comunidad, en un lugar como éste que simboliza parte importante de lo que estamos haciendo como Gobierno Regional, como Gobierno del Presidente Ricardo Lagos, reafirmando así nuestro compromiso por el progreso de la región.</p>
<p class="noticia-e2" align="justify">
En los costados del escenario, están los miembros del Gabinete Regional, compuesto por los secretarios y secretarias ministeriales, los gobernadores provinciales de Limarí, Choapa y Elqui, así como directores y directoras de servicio que representan la labor realizada por cada una de las entidades del Gobierno.  Del mismo modo, nos acompañan miembros del Consejo Regional que han querido estar junto a la comunidad y que juegan un rol trascendental en el desarrollo de nuestra región.</p>
<p class="noticia-e2" align="justify">
Y aquí en el centro, están representantes de las fuerzas vivas de la región; de los distintos actores de nuestra comunidad, personas que han trabajado y aportado en nuestra gestión, sin los cuales ninguna de las acciones desarrolladas desde el gobierno, tendría sentido.</p>
<p class="noticia-e2" align="justify">
Quiero iniciar esta cuenta contándoles que hay algo de lo que me siento contento respecto de lo que hemos hecho como gobierno en nuestra región. Hoy me siento satisfecho con el clima positivo que hemos construido entre todos. Percibo un clima de diálogo, de conversación y colaboración. Juntos estamos construyendo nuestro futuro.</p>
<p class="noticia-e2" align="justify">
Y esto lo decimos desde este nuevo liceo en construcción, desde este lugar que representa nuestro accionar como gobierno de la concertación de partidos por la democracia. Estamos en un entorno nuevo y moderno, todavía en construcción, pero que genera igualdad de oportunidades para todas y todos, en uno de los sectores poblacionales más grandes y significativos de la capital regional.</p>
<p class="noticia-e2" align="justify">
Para nosotros, hablar de desarrollo significa que nuestras instituciones democráticas sean sólidas, que las personas participen activamente; que tengamos probidad, eficacia, responsabilidad y excelencia en la administración pública; que seamos pluralistas y respetuosos de la diversidad; y que el crecimiento económico sea estable y contribuya a la integración social, con más y mejores oportunidades sobretodo para los sectores más pobre, los más vulnerables.</p>
<p class="noticia-e2" align="justify">
Por eso, me siento conforme con la capacidad de la economía regional para generar empleos. Durante el 2003, la temporada de invierno no afectó fuertemente las tasas de desocupación. 
En los últimos tres meses, las cifras nos hablan de la creación de 870 nuevos puestos de trabajo. Por ello, hoy muchas personas están prefiriendo vivir en nuestra región, lo que junto con aumentar nuestra fuerza de trabajo, constituye también una oportunidad para iniciar nuevos emprendimientos y desafíos para generar nuevos puestos de trabajo. </p>
<p class="noticia-e2" align="justify">
Esta es una región que se moderniza con el concurso de todos los sectores, en una alianza público – privada que privilegia por encima de todo, la dignidad de las personas, donde nosotros cumplimos con nuestro rol de catalizadores de las iniciativas; y se gobierna para y con las personas.</p>
<p class="noticia-e2" align="justify">
Es este clima de colaboración, de alianza entre el sector público-privado, pero con un componente fuertemente social, de trabajo conjunto del Estado con la comunidad, lo que me tiene más entusiasmado.  Esta forma de hacer gestión de Gobierno en la cual todos aportamos al desarrollo de la región, nos permite hablar de progreso y oportunidad para todos.</p>
<p class="noticia-e2" align="justify">
De ahí la frase que nos acompaña al fondo: “Contigo construimos región”.

			 </p>
